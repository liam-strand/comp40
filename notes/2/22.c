/* 
 * 
 * In addition to the normal operators (+, -, *, /), we also have the shift
 * operators: (<<, >>).
 * 
 * << is the same thing as multiplying by 2
 * >> is the same thing as dividing by 2
 * 
 * We also have the ~ (bitwise not), which flips all the bits
 * 
 * We also have the & (bitwise and), which takes only the 1s where there is a
 *                                   1 in two inputs.
 * We also have the |  (bitwise or), which takes all the 1s where there is a 1
 *                                   in either of the inputs.
 * 
 * 
 * 
 */