/* 
 * Locality
 * 
 * We can exploit this to make our programs faster
 * 
 * Spatial locality:
 *      When blocks can be cached fewer times, and the important data
 *      is in the cache.
 *      The point is fewer trips back to main memory
 * 
 * Temporal locality:
 *      When you access a piece of memory, it is likely that it will be 
 *      accessed in the near future.
 * 
 * 
 * 
 */